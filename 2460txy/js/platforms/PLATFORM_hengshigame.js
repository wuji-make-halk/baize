var cpOpenid;
var cpPlatform;
var cpGameid;
var cpParams

var pf = function(g2b, shareInfo, pf_params, passData) {
    this.g2b = g2b;
    this.shareInfo = shareInfo;
    this.pf_params = pf_params;

    this.passData = passData;
    p_name = passData.passId;
    this.reyunurl = "";
    this.init();
};
pf.prototype = new platform();

pf.prototype.init = function() {
    cpOpenid = this.pf_params.openid;
    cpPlatform = this.pf_params.platform;
    cpGameid = this.pf_params.gameid;
    cpParams = this.pf_params.params;
    this.g2b.loadScript('http://sharesdk.shishagame.com/api/sharesdk.js', function() {
        this.g2b.postMessage(this.g2b.MESSAGES.INIT_CALLBACK);
    });

};

pf.prototype.pay = function(amount, orderData) {
    var param = {};
    param.openId = orderData.openId;
    param.openKey = orderData.openKey;
    param.appId = this.passData.appId;
    param.money = amount;
    param.orderNo = orderData.orderNo;
    // param.orderNo = orderData.orderNo;
    param.ext = orderData.ext || "";
    param.data = orderData.actor_id; //  put actor_id into data
    param.goodsName = orderData.subject;
    param.cproleid = orderData.cproleid;
    param.platform = 'huangshigame';
    var search = this.g2b.object2search(param);
    this.g2b.createPay({
        search: search
    }, function(res) {
        var generate_order_id = res.d.order_id;
        var user_id = res.d.userId;
        var readl_money = amount / 100;
        // var readl_money = 0.01;
        var subject = orderData.subject;
        var shareobj = new Object();
        shareobj.platform = cpPlatform;
        shareobj.openid = cpOpenid;
        shareobj.gameid = cpGameid;
        shareobj.serverid = param.ext;
        shareobj.tradename = subject;
        shareobj.recharge = readl_money;
        shareobj.custom = generate_order_id;
        shareobj.timestamp = Date.parse(new Date()) / 1000;
        shareobj.params = decodeURIComponent(cpParams);
        console.log(JSON.stringify(shareobj));
        sharepay(shareobj);
        closePayWindow();

    });
};
