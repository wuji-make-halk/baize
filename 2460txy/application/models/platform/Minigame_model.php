    // return order and do the sign varification
    public function get_order_id()
    {
        $order_id = $this->input->get_post('trade_no');
        $total_fee = $this->input->get_post('amount');
        $trade_no = $this->input->get_post('trade_no');
        log_message('debug', $this->platform.' od is'.$order_id.' td'.$trade_no);
        if ($order_id && $total_fee) {
            $condition = array('u_order_id' => $order_id);
            $this->load->model('Game_order_model');

            $game_order = $this->Game_order_model->get_one_by_condition($condition);
            if ($game_order) {
                if (intval($total_fee) == $game_order->money) {
                    return $order_id;
                } else {
                    log_message('debug', $this->platform.' get_order_id money errory '.$game_order->money." != $total_fee");
                }
            } else {
                log_message('debug', $this->platform.' get_order_id error order not found by '.$order_id);
            }
        } else {
            log_message('debug', $this->platform.' get_order_id error order_id or money null');
        }

        return $order_id;
    }

